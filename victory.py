# Список знаменитостей и их годов рождения (в комментариях правильные ответы)
знаменитости = {
    "А.С. Пушкин": 1799,  # Пушкин родился в 1799 году
    "Л.Н. Толстой": 1828,  # Толстой родился в 1828 году
    "И.С. Тургенев": 1818,  # Тургенев родился в 1818 году
    "Ф.М. Достоевский": 1821,  # Достоевский родился в 1821 году
    "И.П. Павлов": 1849  # Павлов родился в 1849 году
}

правильные_ответы = 0
неправильные_ответы = 0


def victory():
    правильные_ответы = 0
    неправильные_ответы = 0

    # Спрашиваем у пользователя год рождения для каждой знаменитости
    for имя, год in знаменитости.items():
        ответ = int(input(f"Введите год рождения {имя}: "))
        if ответ == год:
            правильные_ответы += 1
        else:
            неправильные_ответы += 1

    # Подсчет результатов
    всего_вопросов = len(знаменитости)
    процент_правильных = правильные_ответы * 100 / всего_вопросов
    процент_неправильных = неправильные_ответы * 100 / всего_вопросов

    # Вывод результатов
    print(f"Количество правильных ответов: {правильные_ответы}")
    print(f"Количество неправильных ответов: {неправильные_ответы}")
    print(f"Процент правильных ответов: {процент_правильных:.2f}%")
    print(f"Процент неправильных ответов: {процент_неправильных:.2f}%")


# Основной цикл программы
while True:
    victory()

    # Спрашиваем, хочет ли пользователь начать заново
    while True:
        снова = input("Хотите начать игру сначала? (да/нет): ").strip().lower()
        if снова == "да":
            break  # Начинаем игру снова
        elif снова == "нет":
            print("Игра окончена.")
            exit()  # Выходим из программы
        else:
            print("Неверный ввод. Пожалуйста, введите 'да' или 'нет'.")